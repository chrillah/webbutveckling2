// Licenser bygger på avtalsrätt och upphovsrätt.
// Upphovsrätten sätter gränserna för licenserna och ger skydd.
// Licenser kan inte avtala bort rättigheter som ges av upphovsrätten.
// Licensbrott kan behandlas som upphovsrättsbrott.
// Upphovsmannen måste alltid erkännas och kan motsätta sig kränkande användning eller ändringar i verket.
// Upphovsrätt och avtalsrätt är komplexa områden.
// Det är viktigt att bevara balansen mellan användning och rättigheter.
// Upphovsrätten är inte för att straffa, utan för att främja konst och kultur.
// Licenserna hjälper användare att dela och använda upphovsrättsskyddat material.
