// s.9 Kreativt skapande och upphovsrättslagen
// upphovsrättslagstiftning - finns för att skydda rättsligt
// sådant som är uttryck för konstnärligt och litterärt skapelser
// Reglerna inom upphosvrätten, ger skaparna rätten att bestämma hur deras verk får användas
// Det gäller från och med automatiskt när verket uppkommer
// copyrightssymbolen som oftast används gäller inte i sverige, men fyller ett syfte om att påminna om upphovsrätten
// upphovsrätten skapar en lönsam ensamrätt, och en viktig defintionsfråga om vad literär och konstnärlig verk innebär
// Lagen listar, vad som skyddas av upphovsrättslagstiftielsen:
// skönlitterär eller beskrivande framställning i skrift eller tal
// datorprogram
// musikaliskt eller sceniskt verk
// filmverk
// fotografiskt verk eller något alster av bildkonst, alster av byggnadskonst eller bildkonst,
// kartor
// beskrivande verk i grafik, teckning eller plastik form

// vad lagen inte skyddar:
// enkla skapelser som enkla texter eller blanketter
// inte fakta eller idéer som verk kan innehålla, bara personliga sätt att uttrycka idéerna
// upphovsrätten gäller i 70:e året efter upphosvmannens dödsår
// ideella rättigheter som innefattar rätten att bli namngiven och att motsätta sig ändringar eller kränkande användning av verket.
// ekonomiska rättigheter inkluderar rätten att kopiera och sprida verket
// ideella rättigheter akn inte överlåtas eller licdensieras medan ekonomiska rättigheter kan

// 4 olika sätt at göra ett verk tillgängligt för allmänheten, vilket är en del av upphovsmannens ekonomiska rättigheter:
// 1) genom överföring från en annan plats till allmänheten, där individer kan få tillgång till verket från valfri plats eller tidpunkt
// 2) genom offentligt framförande på samma plats där allmänheten kan ta del av verket, med eller utan tekniskt hjälpmedel
// 3) Genom offentligt visning av verkets exemplar på samma plats där allmänheten kan se det, utan tekniskt hjälpmedel. om hjälpmedel används betraktas det som offentligt framförande
// 4) genom försääljning, uthyrning, utlåning, eller annan spridning av verkets exemplar till allmänheten

// om flera är med och skapar något som faller inom upphovsrätten, och det är svårt att särskilja vemm som gjorde vad, så har de gemensamt rätt till verket
// och delar på ägandeskapet och rättigheterna till verket
// när det gäller en samling av verk eller delar av ett verk kombineras så är personen som skapar samlingen upphovsrätten till själva samlingsverket
//  men det gäller inte de ursprungliga upphovsmännen rättigheter till sina individuella bidrag, de behåller rättigheterna till sina delar av samlingen

// vad upphovsrätten inte skyddar:
// författningar
// myndigheters yytranden och beslut samt officiella översättningar av dem
// undantag finns:
// kartor, bildkonst, dikter och musikaliska verk som ändå skyddas.

// privat bruk:
// tillåter privatpersoner att kopiera för sin egna privata användning utan att be upphovsmannes om tillstånd
// det inkluderar nära familj och vänner och kopierignen är begränsade till mindre omfattande verk eller delar av verk
// verken måste ha offentligtgjorts enligt upphovsrättslagstiftningen för att tillåta kopiering för prvat bruk
// gäller inte kopiera byggnadsverk, datorprogram eller digitala sammanställningar för privat bruk
// det ger heller inte rätten att låta andra kopiera musik, film eller imititioner av konstverk

// Citaträtten:
// tillåter att citera ur upphovsrättesligt skyddade verk i enlighet med god sed och ändamålet. Texter kan oftast citeras medan visuella konstverk inte kan citeras utan tillsttånd
// Återgivning av konstverk är tillåtet i vissa fall, som veteskapliga framställningar, kritiska textet (ej digitalt) och tidnigsartiklar om dagshändelser men endast i begränsad omfattning och enligt god sed

// Konsumenter och producenter
// upphosvrätten fokuserar på att skydda produkten än den kreativa processen
// kravet att söka tillstånd för att använda andras verk försvårar och fördröjer skapandet av nya verk.
// delande av skapelser betraktas som avvikande, trots att stora delar av upphosvrätten kan avtalas bort

